public class oddeven
{
    public static void main(String args[])
    {
        int i,j;
        System.out.println("Odd");
        for(i=1;i<=10;i=i+2)
        {
            System.out.println(+i);
        }
        System.out.println("Even");
        for(j=0;j<=10;j=j+2)
        {
            System.out.println(+j);
        }
    }
}
import java.math.*;
public class math5func
{
   public static void main(int log,int abs,int sqrt,int cbrt)
   {
       int i;
       System.out.println("Log Value");
       System.out.println(Math.log(log));
       System.out.println("Absolute Value");
       System.out.println(Math.abs(abs));
       System.out.println("Square root Value");
       System.out.println(Math.sqrt(sqrt));
       System.out.println("Cube root Value");
       System.out.println(Math.cbrt(cbrt));
       System.out.println("Random between 0 & 1");
       for(i=1;i<=1;i++)
       {
           double d=Math.random();
           System.out.println(d);
       }
   }
}

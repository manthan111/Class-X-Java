public class Arraychar
{
    public static void main(String args[])
    {
        char c[]={'c','o','m','p','u','t','e','r'};
        int i;
        System.out.println("The character of array element are: ");
        for(i=0;i<8;i++)
        {
            System.out.println(c[i]);
        }
    }
}
